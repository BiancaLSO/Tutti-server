import { Prop, Schema, SchemaFactory } from '@nestjs/mongoose';
import { Document } from 'mongoose';

export type UserDocument = User & Document;

@Schema()
export class User {
  @Prop()
  username: string;

  @Prop({ required: true })
  email: string;

  @Prop({ required: true })
  password: string;

  @Prop({ required: true })
  confimrPassword: string;

  @Prop({ required: true })
  isMusician: boolean;
}

export const UserSchema = SchemaFactory.createForClass(User);
